    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed
        // TODO add your handling code here:
        String arq = "conf.txt";
        String texto = Arquivo.Read(arq);
        char[] password =jPasswordFieldSenha.getPassword();
        String senhaDigitada = new String(password);
        String senha = texto;
        
        if(texto.isEmpty()){
            JOptionPane.showMessageDialog(this, "Erro ao ler do arquivo");
        }

        else if(senhaDigitada.equalsIgnoreCase(senha)){
            Config novajanela = new Config();
            novajanela.setVisible(true); 
            dispose();
        }

        else {
            jLabelSenhaErrada.setText("Senha Inv√°lida");
            jPasswordFieldSenha.setText("");
        }    
    }//GEN-LAST:event_jButtonEntrarActionPerformed

