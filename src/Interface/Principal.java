    private void jButtonChamarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChamarSenhaActionPerformed
        // TODO add your handling code here:
        if (jTableSenhas.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this, "Selecione uma senha");
        }
        else{
            jLabelSenhaAtual.setForeground(Color.WHITE);
            jLabelSenhaAtual.setText(jTableSenhas.getValueAt(jTableSenhas.getSelectedRow(), 0).toString());
            TelaPainel.jLabelSenhaAtual.setForeground(Color.WHITE);
            TelaPainel.jLabelSenhaAtual.setText(jTableSenhas.getValueAt(jTableSenhas.getSelectedRow(), 0).toString());

            //Adicionando senha na no array
            SenhasChamadas.add(jTableSenhas.getValueAt(jTableSenhas.getSelectedRow(), 0).toString());

            //Removendo a senha chamada da jTable
            ((DefaultTableModel)jTableSenhas.getModel()).removeRow(jTableSenhas.getSelectedRow());

            if (SenhasChamadas.size()==2){
                jLabelSenhaAnt.setText(SenhasChamadas.get(0));
                TelaPainel.jLabelSenhaAnt.setText(SenhasChamadas.get(0));
                regSenha = SenhasChamadas.get(1);

            }
            else if (SenhasChamadas.size()==3){
                jLabelSenhaAnt.setText(SenhasChamadas.get(1));
                jLabelSenhaAnt2.setText(SenhasChamadas.get(0));
                TelaPainel.jLabelSenhaAnt.setText(SenhasChamadas.get(1));
                TelaPainel.jLabelSenhaAnt2.setText(SenhasChamadas.get(0));
                regSenha = SenhasChamadas.get(2);

            }
            else if (SenhasChamadas.size()==4){
                jLabelSenhaAnt.setText(SenhasChamadas.get(2));
                jLabelSenhaAnt2.setText(SenhasChamadas.get(1));
                jLabelSenhaAnt3.setText(SenhasChamadas.get(0));
                regSenha = SenhasChamadas.get(3);
                TelaPainel.jLabelSenhaAnt.setText(SenhasChamadas.get(2));
                TelaPainel.jLabelSenhaAnt2.setText(SenhasChamadas.get(1));
                TelaPainel.jLabelSenhaAnt3.setText(SenhasChamadas.get(0));
                SenhasChamadas.remove(0);

            }

        }
    }//GEN-LAST:event_jButtonChamarSenhaActionPerformed

