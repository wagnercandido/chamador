    private void jButtonSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSairActionPerformed
        // TODO add your handling code here:
        Integer opcao = JOptionPane.showConfirmDialog(this, "Deseja Realmente Sair?","ConfirmaÃ§Ã£o de SaÃ­da", JOptionPane.OK_CANCEL_OPTION);
        if (opcao!=2 && opcao!=-1){
            if (RegistroSenhas.size()==0){
                System.exit(0);
            }
            else{
                Object contador;
                contador = RegistroSenhas.get(RegistroSenhas.size() - 1);

                String arq = "senha.txt";
                String texto = contador.toString();

                if(Arquivo.Write(arq, texto))
                JOptionPane.showMessageDialog(this, "Arquivo salvo com sucesso");
                else
                JOptionPane.showConfirmDialog(this, "Erro ao salvar o arquivo");

                System.exit(0);
            }
        }
    }//GEN-LAST:event_jButtonSairActionPerformed

    private void jButtonNovaSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNovaSenhaActionPerformed
        // TODO add your handling code here:

        if (numSenha==1){
            String arq = "senha.txt";
            String texto = Arquivo.Read(arq);
            if(texto.isEmpty())
            JOptionPane.showMessageDialog(this, "Erro ao ler do arquivo");
            else
            numSenha = Integer.parseInt(texto);
            //JOptionPane.showMessageDialog(this, texto);
            numSenha++;
        }
        else{
            DefaultTableModel tabela = (DefaultTableModel) jTableSenhas.getModel();
            Object[] senhas = {numSenha};
            tabela.addRow(senhas);
            regSenha = Integer.toString(numSenha);
            RegistroSenhas.add(regSenha);
            numSenha++;
        }
    }//GEN-LAST:event_jButtonNovaSenhaActionPerformed

